    private boolean validarCampos() {
        if (jTextFieldNome.getText().trim().length() == 0) {
            this.exibirInformacao("O valor do campo 'Nome' não foi informado.");
            jTextFieldNome.requestFocus();
            return false;
        }
        if (jTextFieldDataNascimento.getText().length() != 0) {
            try {
                dateFormat.parse(jTextFieldDataNascimento.getText());
            } catch (ParseException ex) {
                this.exibirInformacao("O valor do campo 'Data de Nascimento' é inválido.");
                jTextFieldDataNascimento.requestFocus();
                return false;
            }
        }
        try {
            Double.parseDouble(jTextFieldAltura.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Altura' é inválido.");
            jTextFieldAltura.requestFocus();
            return false;
        }
        try {
            Double.parseDouble(jTextFieldPeso.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Peso' é inválido.");
            jTextFieldPeso.requestFocus();
            return false;
        }
        if (!jTextFieldNumero.getText().equals("")) {
            try {
                Integer.parseInt(jTextFieldNumero.getText());
            } catch (Exception ex) {
                this.exibirInformacao("O valor do campo 'Número' é inválido.");
                jTextFieldNumero.requestFocus();
                return false;
            }
        }
        try {
            Integer.parseInt(jTextFieldDerrotas.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Total de Derrotas' é inválido.");
            jTextFieldDerrotas.requestFocus();
            return false;
        }
        try {
            Integer.parseInt(jTextFieldVitoriasFora.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Total de Vitórias Fora de Casa' é inválido.");
            jTextFieldVitoriasFora.requestFocus();
            return false;
        }
        try {
            Integer.parseInt(jTextFieldEmpates.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Total de Empates' é inválido.");
            jTextFieldEmpates.requestFocus();
            return false;
        }
        try {
            Integer.parseInt(jTextFieldPartidas.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Total de Partidas' é inválido.");
            jTextFieldPartidas.requestFocus();
            return false;
        }
        try {
            Integer.parseInt(jTextFieldGols.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Total de Gols' é inválido.");
            jTextFieldGols.requestFocus();
            return false;
        }
        try {
            Integer.parseInt(jTextFieldVitoriasCasa.getText());
        } catch (Exception ex) {
            this.exibirInformacao("O valor do campo 'Total de Vitórias Em Casa' é inválido.");
            jTextFieldVitoriasCasa.requestFocus();
            return false;
        }
        return true;
    }
    
    private void salvarRegistro() {
        Endereco endereco;
        ArrayList<String> telefones;
        ArrayList<Premiacao> premiacoes;
        Premiacao premiacao;
        Date dataNascimento;

        if (this.validarCampos() == false) {
            return;
        }

        if (jTextFieldDataNascimento.getText().length() == 0) {
            dataNascimento = null;
        } else {
            try {
                dataNascimento = dateFormat.parse(jTextFieldDataNascimento.getText());
            } catch (ParseException ex) {
                exibirInformacao("Falha ao gravar a data de nascimento: " + ex.toString());
                return;
            }
        }

        endereco = new Endereco();
        endereco.setBairro(jTextFieldBairro.getText());
        endereco.setCep(jTextFieldCEP.getText());
        endereco.setCidade(jTextFieldCidade.getText());
        endereco.setComplemento(jTextFieldComplemento.getText());
        endereco.setEstado((String) jComboBoxEstado.getSelectedItem());
        endereco.setLogradouro(jTextFieldLogradouro.getText());
        endereco.setNumero(jTextFieldNumero.getText());
        endereco.setPais(jTextFieldPais.getText());

        telefones = new ArrayList<String>();
        for (int i = 0; i < telefonesListModel.size(); i++) {
            telefones.add(telefonesListModel.getElementAt(i).toString());
        }
        
        premiacoes = new ArrayList<Premiacao>();
        for (int i = 0; i < premiacaoListModel.size(); i++) {
            premiacao = (Premiacao) premiacaoListModel.getElementAt(i);
            premiacoes.add(premiacao);            
        }

        if (novoRegistro == true) {
            umJogadorFutebol = new JogadorFutebol(jTextFieldNome.getText());
        } else {
            umJogadorFutebol.setNome(jTextFieldNome.getText());
        }
        umJogadorFutebol.setEndereco(endereco);
        umJogadorFutebol.setTelefones(telefones);
        umJogadorFutebol.setPremiacoes(premiacoes);
        umJogadorFutebol.setDataNascimento(dataNascimento);
        umJogadorFutebol.setAltura(Double.parseDouble(jTextFieldAltura.getText()));
        umJogadorFutebol.setNomeMae(jTextFieldNomeMae.getText());
        umJogadorFutebol.setNomePai(jTextFieldNomePai.getText());
        umJogadorFutebol.setPeso(Double.parseDouble(jTextFieldPeso.getText()));
        umJogadorFutebol.setCpf(jTextFieldCPF.getText());
        umJogadorFutebol.setRg(jTextFieldRG.getText());
        umJogadorFutebol.setTotalDerrotas(Integer.parseInt(jTextFieldDerrotas.getText()));
        umJogadorFutebol.setTotalVitoriasForaCasa(Integer.parseInt(jTextFieldVitoriasFora.getText()));
        umJogadorFutebol.setTotalVitoriasEmCasa(Integer.parseInt(jTextFieldVitoriasCasa.getText()));
        umJogadorFutebol.setTotalEmpates(Integer.parseInt(jTextFieldEmpates.getText()));
        umJogadorFutebol.setTotalJogos(Integer.parseInt(jTextFieldPartidas.getText()));
        umJogadorFutebol.setTotalGols(Integer.parseInt(jTextFieldGols.getText()));        

        switch (jComboBoxSexo.getSelectedIndex()) {
            case SEXO_MASCULINO_INDICE:
                umJogadorFutebol.setSexo(SEXO_MASCULINO_VALOR);
                break;
            case SEXO_FEMININO_INDICE:
                umJogadorFutebol.setSexo(SEXO_FEMININO_VALOR);
                break;
        }

        switch (jComboBoxCategoria.getSelectedIndex()) {
            case CATEGORIA_AMADOR_INDICE:
                umJogadorFutebol.setCategoria(CATEGORIA_AMADOR_VALOR);
                break;
            case CATEGORIA_PROFISSIONAL_INDICE:
                umJogadorFutebol.setCategoria(CATEGORIA_PROFISSIONAL_VALOR);
                break;
        }

        switch (jComboBoxClasse.getSelectedIndex()) {
            case CLASSE_CHUPETINHA_INDICE:
                umJogadorFutebol.setClasse(CLASSE_CHUPETINHA_VALOR);
                break;
            case CLASSE_FRALDINHA_INDICE:
                 umJogadorFutebol.setClasse(CLASSE_FRALDINHA_VALOR);
                break;
            case CLASSE_MIRIM_INDICE:
                 umJogadorFutebol.setClasse(CLASSE_MIRIM_VALOR);
                break;
            case CLASSE_INFANTIL_INDICE:
                 umJogadorFutebol.setClasse(CLASSE_INFANTIL_VALOR);
                break;
            case CLASSE_JUVENIL_INDICE:
                 umJogadorFutebol.setClasse(CLASSE_JUVENIL_VALOR);
                break;
            case CLASSE_JUNIOR_INDICE:
                 umJogadorFutebol.setClasse(CLASSE_JUNIOR_VALOR);
                break;
            case CLASSE_ADULTO_INDICE:
                 umJogadorFutebol.setClasse(CLASSE_ADULTO_VALOR);
                break;
            case CLASSE_MASTER_INDICE:
                 umJogadorFutebol.setClasse(CLASSE_MASTER_VALOR);
                break;
        }

        if (novoRegistro == true) {
            controleJogadorFutebol.adicionar(umJogadorFutebol);
        }
        modoAlteracao = false;
        novoRegistro = false;
        this.carregarListaJogadoresFutebol();
        this.habilitarDesabilitarCampos();
    }
    
